import axios  from 'axios';
export default {
    name: 'interface-test',
    data(){
        return{
            columns: [
                {
                    type: 'selection',
                    width: 60,
                    align: 'center'
                },
                {
                    title: '用例名称',
                    key: 'name',
                },
                {
                    title: '用例描述',
                    key: 'case_describe'
                },
                {
                    title: '操作',
                    key: 'action',
                    width: 350,
                    align: 'center',
                    render: (h, params) => {
                        return h('div', [
                            h('Button', {
                                props: {
                                    type: 'primary',
                                    size: 'small'
                                },
                                style: {
                                    marginRight: '5px'
                                },
                                on: {
                                    click: () => {
                                        this.$router.push({
                                        path:"/test-manage/interface-case-edit",
                                        query:{projectId:this.$route.query.projectId,applicationId:this.$route.query.applicationId,caseId:params.row.id}})
                                    }
                                }
                            }, '编辑'),
                            h('Button', {
                                props: {
                                    type: 'primary',
                                    size: 'small'
                                },
                                on: {
                                    click: () => {
                                        this.removeCaseData.caseId = params.row.id
                                        this.removeCase()
                                    }
                                }
